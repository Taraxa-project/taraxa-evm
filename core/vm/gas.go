func gasCall(evm *EVM, contract *Contract, stack *Stack, mem *Memory, memorySize uint64) (uint64, error) {
	var (
		gas            = evm.gas_table.Calls
		transfersValue = stack.Back(2).Sign() != 0
		acc            = evm.get_account(stack.Back(1))
	)
	if evm.rules.IsEIP158 {
		if transfersValue && acc.IsEIP161Empty() {
			gas += CallNewAccountGas
		}
	} else if !acc.IsNotNIL() {
		gas += CallNewAccountGas
	}
	if transfersValue {
		gas += CallValueTransferGas
	}
	memoryGas, err := memoryGasCost(mem, memorySize)
	if err != nil {
		return 0, err
	}
	var overflow bool
	if gas, overflow = math.SafeAdd(gas, memoryGas); overflow {
		return 0, errGasUintOverflow
	}

	evm.call_gas_tmp, err = callGas(evm.gas_table, contract.Gas, gas, stack.Back(0))
	if err != nil {
		return 0, err
	}
	if gas, overflow = math.SafeAdd(gas, evm.call_gas_tmp); overflow {
		return 0, errGasUintOverflow
	}
	return gas, nil
}

